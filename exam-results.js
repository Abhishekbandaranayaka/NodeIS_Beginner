/*
A = Exellent
B = Very Good
C = Good
S = Pass
W = Fail
*/
const input = "B";
if(input==="A"){
    console.log("Exellent");
}else if(input==="B"){
    console.log("Very Good");
}else if(input==="C"){
    console.log("Good");
}else if(input==="S"){
    console.log("Pass");
}else if(input==="W"){
    console.log("Fail");
}else{
    console.log("Input is not valid");
}
